    <h3>Pelíšky</h3>
    <div class="film">
        <div class="foto"><img src="img/pelisky.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>Film Pelíšky je česká komediální klasika z roku 1999, kterou režíroval Jan Hřebejk podle scénáře Petra Jarchovského. Děj se odehrává na přelomu 60. a 70. let a s humorem i nostalgií zachycuje život dvou rodin v období před invazí vojsk Varšavské smlouvy.</p>
    </div>
    <h3>Adéla ještě nevečeřela</h3>
    <div class="film">
        <div class="foto"><img src="img/adela-jeste-nevecerela.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>Film Adéla ještě nevečeřela je kultovní česká filmová komedie z roku 1977, která s nadhledem a parodií spojuje detektivní žánr s hororem. Děj se odehrává v tajemné Praze konce 19. století a sleduje slavného amerického detektiva Nicka Cartera, jenž přijíždí vyšetřit záhadné zmizení. Film vyniká chytrým humorem, stylizovanými dialogy, originálními postavami a nezapomenutelným masojedlým květem Adélou. Díky precizní režii Oldřicha Lipského, hereckým výkonům (zejména Rudolfa Hrušínského a Michala Dočolomanského) a osobité atmosféře patří snímek mezi nejvýraznější a nejoblíbenější díla české kinematografie.</p>
    </div>
