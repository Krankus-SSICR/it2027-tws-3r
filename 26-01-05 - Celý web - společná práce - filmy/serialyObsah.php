    <h3>The Rookie</h3>
    <div class="film">
        <div class="foto"><img src="img/the-rookie.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>The Rookie je americký kriminální seriál, který sleduje příběh Johna Nolana, čtyřicetiletého muže, jenž se
            rozhodne začít nový život jako nejstarší nováček u losangeleské policie. Seriál kombinuje napínavé policejní
            případy s osobními výzvami hlavního hrdiny, který se musí vyrovnat s fyzickou i psychickou náročností práce
            a s nedůvěrou okolí. Díky realistickému pohledu na policejní práci, silným postavám a vyvážené dávce dramatu
            i humoru nabízí The Rookie nejen akční podívanou, ale i inspirativní příběh o druhých šancích a vytrvalosti.
        </p>
    </div>
    <h3>Gilmorova děvčata</h3>
    <div class="film">
        <div class="foto"><img src="img/gilmorova-devcata.jpg" alt="Fotka seriálu"></div>
        <div class="rozdeleni"></div>
        <p>Gilmorova děvčata jsou populární americký seriál, který se zaměřuje na vztah svobodné matky Lorelai a její
            dcery Rory. Děj se odehrává v malebném městečku Stars Hollow a je plný rychlých dialogů, humoru a emocí.
            Seriál se dotýká témat rodiny, přátelství, dospívání i hledání vlastní cesty a je oblíbený především pro své
            sympatické postavy a pohodovou atmosféru.</p>
    </div>

    <h3>Štěstíčku naproti</h3>
    <div class="film">
        <div class="foto"><img src="img/stesticku-naproti.jpg" alt="Fotka seriálu"></div>
        <div class="rozdeleni"></div>
        <p>Zkušený policejní vyšetřovatel a trenér fotbalového potěru Marek se po letech z neznámých důvodů vrací z Prahy do Brna. Šéf brněnské expozitury protikorupční policie mu hned přidělí na první pohled bezvýznamný případ úplatků v nižší fotbalové soutěži. A Marek začne pomocí telefonních odposlechů postupně rozplétat komplikovanou korupční kauzu sahající až do první ligy. Zároveň s tím sledujeme zkušeného fotbalového manažera Ivana, který se pokouší sehnat chybějící miliony na koupi rozdílového hráče, jehož sestupem ohrožený Žižkov nutně potřebuje. Trabl spočívá v tom, že prodávajícím je majitel konkurenčního klubu Čestmír, který na ty peníze zoufale čeká a roztrhne Ivana jak hada, jestli je nedostane včas!</p>
    </div>

