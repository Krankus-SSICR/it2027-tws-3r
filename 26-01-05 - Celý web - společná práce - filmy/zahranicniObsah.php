    <h2>Zahraniční filmy</h2>
    <h3>Catch Me If You Can</h3>
    <div class="film">
        <div class="foto"><img src="img/catch-me-if-you-can.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>Catch Me If You Can je film z roku 2002 režiséra Stevena Spielberga, který vypráví skutečný příběh Franka
            Abagnalea Jr., mladého podvodníka, jenž se v 60. letech vydával za pilota, lékaře i právníka a podváděl
            banky po celém světě. V hlavních rolích se představují Leonardo DiCaprio jako charismatický a vynalézavý
            Frank a Tom Hanks jako neúnavný agent FBI Carl Hanratty, který ho pronásleduje. Film kombinuje prvky
            kriminálního dramatu a lehkého humoru a kromě napínavého honu kočky s myší se dotýká i témat identity,
            rodiny a touhy po uznání.</p>
    </div>

    <h3>Titanic</h3>
    <div class="film">
        <div class="foto"><img src="img/titanic.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>Titanic je slavný romantický film z roku 1997 režiséra Jamese Camerona, který vypráví tragický příběh lásky
            mezi chudým umělcem Jackem a bohatou dívkou Rose na palubě luxusní lodi Titanic. Film je známý svým
            emotivním dějem, působivými vizuálními efekty a silnou hudbou. Kromě romantické linie ukazuje také sociální
            rozdíly a osudovou katastrofu, která se stala jednou z nejznámějších v historii.</p>
    </div>

    <h3>Pretty Woman</h3>
    <div class="film">
        <div class="foto"><img src="img/pretty-woman.jpg" alt="Fotka filmu"></div>
        <div class="rozdeleni"></div>
        <p>Pretty Woman je romantická komedie z roku 1990, která vypráví příběh bohatého podnikatele Edwarda a mladé
            ženy Vivian, jež se živí jako společnice. Jejich setkání postupně přeroste v nečekaný vztah, který překonává
            společenské rozdíly. Film je známý především díky hereckým výkonům Julie Roberts a Richarda Gerea a stal se
            jedním z nejikoničtějších romantických filmů všech dob.</p>
    </div>

