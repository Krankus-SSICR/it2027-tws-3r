<section>
    <h2>🏛️ Historie a kultura</h2>
    <p>Finsko má bohatou historii a jedinečnou kulturu, která se liší od ostatních skandinávských zemí. Finská kultura je známá svou minimalistickou estetikou a praktičností.</p>
    
    <h3>Historické milníky:</h3>
    <ul>
        <li><strong>1157</strong> - Finsko se stává součástí Švédska</li>
        <li><strong>1809</strong> - Finsko se stává ruským velkovévodstvím</li>
        <li><strong>1917</strong> - Finsko získává nezávislost</li>
        <li><strong>1939-1944</strong> - Zimní a pokračovací válka se SSSR</li>
        <li><strong>1995</strong> - Finsko se připojuje k Evropské unii</li>
        <li><strong>2002</strong> - Přijetí eura jako měny</li>
    </ul>

    <h3>Kulturní tradice:</h3>
    <ul>
        <li><strong>Sauna</strong> - Finská sauna má více než 2000 let trvání a je součástí finské identity</li>
        <li><strong>Jóulu (Vánoce)</strong> - Finský způsob oslav Vánoc je jedinečný a rodinný</li>
        <li><strong>Kalevala</strong> - Starověká finská epická báseň, která byla inspirací pro spoustu umělců</li>
        <li><strong>Tango</strong> - Tanec tango je klíčovou součástí finské kultury</li>
    </ul>
</section>

