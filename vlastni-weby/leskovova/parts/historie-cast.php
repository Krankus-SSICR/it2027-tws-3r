            <p>Od konce 5. století se na území dnešního Česka objevovali první Slované. V 7. století vytvořily slovanské kmeny pod vedením franského kupce Sáma tzv. Sámovu říši (asi 623–659), jednalo se však spíše o nadkmenový svaz. V letech 830–833 na Moravě, na Slovensku, v severním Maďarsku a na západním Zakarpatsku vznikla pod vládou dynastie Mojmírovců Velkomoravská říše, která postupně zahrnula i Čechy (890–894), Slezsko, Lužici, Malopolsko a zbytek Maďarska. Velkomoravská říše byla již plnohodnotným státním útvarem. Byla christianizována, mj. za pomoci byzantské misie Cyrila a Metoděje, již pozval panovník Rostislav. Za jeho následníka Svatopluka I. říše dosáhla maxima své moci. Po jeho smrti však přišel rychlý pád. Čechy se od Velké Moravy odtrhly v roce 894 a ta byla v roce 906 nebo 907 rozvrácena tehdy ještě kočovnými Maďary.</p>
