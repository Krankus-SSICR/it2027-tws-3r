            <p>Česko, plným názvem Česká republika, je vnitrozemský stát ve střední Evropě. Na jihu sousedí s Rakouskem,
                na západě s Německem, na severovýchodě s Polskem a na jihovýchodě se Slovenskem. Povrch má převažující
                ráz pahorkatin a vrchovin, podnebí je mírné, přechodné mezi kontinentálním a oceánickým typem. Skládá se
                ze tří historických zemí – Čech, Moravy a českého Slezska. Česko se dělí na 14 samosprávných krajů, má
                rozlohu 78 871 km² a žije v něm 10,9 milionu obyvatel. Hlavním a největším městem je Praha; mezi další
                velká města patří Brno, Ostrava, Plzeň, Liberec a Olomouc. Většina obyvatel jsou Češi a Moravané,
                nejvýznamnějšími menšinami jsou Slováci a Ukrajinci. Úředním a nejrozšířenějším jazykem je čeština,
                částečně úředními jsou také slovenština a polština.</p>
