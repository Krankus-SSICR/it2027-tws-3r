    <h3>Vládní situace a premiérka</h3>
    <div class="cast">
    <div class="rozdeleni"></div>
    <p>Itálie je parlamentní republikou a od roku 2022 je premiérkou Giorgia Meloniová, šéfka pravicové strany Bratři Itálie (Fratelli d'Italia). Je to první žena-premiérka v dějinách Itálie a její vláda je považována za jednu z nejpravicovějších od druhé světové války.Meloni má stále silnou pozici a konsolidovala moc po třech letech ve funkci, což znamená relativní politickou stabilitu po období častých vládních pádů v předchozích letech.Hlavní otázkou do budoucna zůstává udržení koalice a příprava na další volby, které se konají nejpozději do prosince 2027.</p>
