    <h1>HISTORICKÁ DATABÁZE</h1>
    <p class="kicker">
        Rok 2077 znamenal konec známé civilizace. Globální konflikt o poslední přírodní zdroje vyústil v rozsáhlou
        jadernou výměnu, která během několika hodin zničila většinu světa. Tato země vznikla v univerzu Fallout po Velké jaderné válce roku 2077. Svět Falloutu popisuje technologicky vyspělou civilizaci, která byla zničena nukleárním konfliktem, po němž vznikly nové státy a společnosti.
    </p>

    <h2>VELKÁ VÁLKA</h2>
    <p class="kicker">
        Jaderný konflikt proběhl během jediného dne a způsobil kolaps vlád, infrastruktury a většiny měst.
        Povrch planety se stal neobyvatelným a lidstvo stálo na pokraji vyhynutí.
    </p>

    <h2>VAULTY</h2>
    <p class="kicker">
        Přeživší se ukryli do podzemních krytů známých jako Vaulty. Tyto kryty byly postaveny společností Vault-Tec
        a sloužily nejen k přežití, ale také k dlouhodobým sociálním a technologickým experimentům.
    </p>

    <h2>ÉRA PUSTINY</h2>
    <ul class="list">
