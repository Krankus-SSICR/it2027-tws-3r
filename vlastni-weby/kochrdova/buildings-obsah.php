        <p>Islandské drnové domy (torfbæir) představují unikátní architektonické řešení, které umožnilo osídlení
            nehostinného severu po více než tisíc let. Tyto stavby, tvořené dřevěnou kostrou a silnými stěnami z
            vrstvených drnů na kamenných základech, vynikají mimořádnými izolačními vlastnostmi, díky nimž udržovaly
            stabilní teplotu i během drsných islandských zim. Hlavním obytným prostorem byla společná místnost baðstofa,
            kde rodiny společně pracovaly i spaly. Ačkoliv se v nich běžně bydlelo až do poloviny 20. století, dnes
            slouží především jako kulturní památky chráněné Islandským národním muzeem, přičemž mezi nejvýznamnější
            zachovalé areály patří Glaumbær nebo Keldur.</p>
