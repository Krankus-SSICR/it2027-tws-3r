        <h2>New York City</h2>
        <p>New York je nejlidnatějším městem USA a je považován za jedno z nejdůležitějších center světa. Leží na východním pobřeží a je známý svými mrakodrapy, sochou Svobody, Central Parkem a finanční čtvrtí Wall Street. Město je kulturním a mediálním centrem s divadly na Broadwayi, slavnými muzei a silným mezinárodním vlivem.</p>

        <img src="img/ny.jpg" alt="new york">


        <h2>Los Angeles</h2>
        <p>Los Angeles se nachází v Kalifornii na západním pobřeží a je světovým symbolem filmového průmyslu díky Hollywoodu. Město je známé také plážemi jako Venice Beach nebo Santa Monica, příjemným klimatem a rozsáhlými předměstími. Je důležitým centrem zábavy, médií a moderních technologií.</p>
        <img src="img/la.jpg" alt="Los Angeles">
        
        
        <h2>Chicago</h2>
        <p>Chicago leží u Michiganského jezera a patří mezi nejvýznamnější města Středozápadu. Proslavilo se svou architekturou, mrakodrapy a hlubokou hudební tradicí, zejména v oblasti jazzu a blues. Město je také důležitým dopravním uzlem a hospodářským centrem s bohatou kulturní scénou.</p>
