        <h2>Geografie a přírodní bohatství:</h2>
        <p>USA mají jednu z nejrozmanitějších krajin na světě. Na východě se nachází Apalačské pohoří, ve středu rozsáhlé Velké planiny a na západě Skalnaté hory i pobřeží Tichého oceánu. Jihozápadní část země je známá pouštěmi, zatímco Aljaška nabízí ledovce a arktickou přírodu. Země má stovky chráněných oblastí a národních parků, mezi nejslavnější patří Yellowstone, Yosemite, Zion nebo Grand Canyon.</p>


        <h2>Obyvatelstvo a jazyk:</h2>
        <p>USA mají více než 330 milionů obyvatel a jsou jednou z nejvíce multikulturních zemí světa. Většina obyvatel pochází z rodin přistěhovalců z Evropy, Afriky, Asie i Latinské Ameriky. Úředním jazykem na federální úrovni není oficiálně stanoven žádný, ale nejrozšířenější je angličtina, následovaná španělštinou. Kulturní rozmanitost se odráží v kuchyni, tradicích i svátcích.</p>
