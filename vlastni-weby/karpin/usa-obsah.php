        <h2>Spojené státy americké (USA)</h2>
        <p>Spojené státy americké leží v Severní Americe a tvoří je 50 států, které mají vlastní vlády, ale podléhají federálním zákonům. Hlavním městem je Washington, D.C., sídlo prezidenta, Kongresu a Nejvyššího soudu. Země vznikla roku 1776 vyhlášením nezávislosti na Velké Británii a patří mezi nejstarší moderní demokracie. Prezident je hlavou státu i vlády a politický systém je založen na dělbě moci mezi výkonnou, zákonodárnou a soudní složkou.</p>



