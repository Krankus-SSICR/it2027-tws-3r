    <h2>Pchjongjang</h2>
    <h3>„Výkladní skříň“ celé země</h3>
    <div class="film">
        <div class="foto"><img src="img/pch.jpg" alt="Fotka"></div>
        <div class="rozdeleni"></div>
        <p>Pchjongjang je pečlivě udržované město, kam mají přístup jen vybraní obyvatelé považovaní za loajální režimu. Má ukazovat, jak se v Severní Koreji „dobře žije“, i když realita jinde v zemi je úplně jiná.</p>
    </div>

    <h3>Obří památníky a prázdné ulice</h3>
    <div class="film">
        <div class="foto"><img src="img/vez.jpg" alt="Fotka"></div>
        <div class="rozdeleni"></div>
        <p>Ve městě je mnoho monumentálních staveb, soch a širokých bulvárů (např. Věž čučche). Přesto bývají ulice často nezvykle prázdné, protože málo lidí vlastní auta.</p>
    </div>

    <h3>Jedno z nejhlubších metra na světě</h3>
    <div class="film">
        <div class="foto"><img src="img/metro.jpg" alt="Fotka"></div>
        <div class="rozdeleni"></div>
        <p>Metro v Pchjongjangu patří k nejhlubším na světě (až kolem 100 metrů pod zemí). Slouží nejen jako doprava, ale i jako kryt v případě války.</p>
    </div>
