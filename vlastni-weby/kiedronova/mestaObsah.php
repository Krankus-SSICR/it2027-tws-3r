    <h2>Města Španělska</h2>
    <h3>Madrid</h3>
    <div class="obsah">
        <div class="foto"><img src="img/madrid.jpg" alt="Fotka Madridu"></div>
        <p>Madrid je živé a elegantní hlavní město Španělska, které spojuje bohatou historii s moderním městským stylem. Návštěvníky láká královský palác, slavné muzeum Prado i rozlehlé parky, jako je Retiro, ideální pro odpočinek uprostřed ruchu města. Typická madridská atmosféra se nejlépe pozná v úzkých uličkách plných tapas barů, kaváren a tradičních restaurací. Madrid je město plné energie, kultury a vášně, které si zamilujete na první návštěvu.</p>
    </div>
    <h3 style="text-align:end;">Barcelona</h3>
    <div class="obsah">
        <p>Barcelona je jedinečné město na pobřeží Středozemního moře, které okouzlí svou architekturou, kulturou i uvolněnou atmosférou. Dominantou města jsou díla Antonia Gaudího, především slavná Sagrada Família a park Güell. Barcelona nabízí krásné pláže, rušnou třídu La Rambla i historickou čtvrť Barri Gòtic. Je to město, kde se snoubí umění, gastronomie a středomořský životní styl.</p>
        <div class="foto"><img src="img/barcelona.jpg" alt="Fotka Barcelony"></div>
    </div>
    <h3>Sevilla</h3>
    <div class="obsah">
        <div class="foto"><img src="img/sevilla.jpg" alt="Fotka Sevilly"></div>
        <p>Sevilla je okouzlující město na jihu Španělska, považované za kolébku flamenca a andaluských tradic. Návštěvníky uchvátí monumentální katedrála s věží Giralda, královský palác Alcázar i malebné uličky čtvrti Santa Cruz. Sevilla dýchá historií, vášní a sluncem, které dodává městu nezaměnitelnou atmosféru plnou barev, hudby a života.</p>
