    <h3>Basketbal</h3>
    <div class="obsah">
        <div class="foto"><img src="img/basketbal.jpg" alt="Fotka basketbalu"></div>
        <p>Basketbal má ve Španělsku silnou pozici a patří k nejúspěšnějším kolektivním sportům v zemi. Španělská liga ACB je považována za jednu z nejlepších v Evropě a vychovala řadu světových hráčů. Národní tým pravidelně dosahuje výrazných úspěchů na evropských i světových šampionátech a basketbal má ve Španělsku širokou fanouškovskou základnu.</p>
