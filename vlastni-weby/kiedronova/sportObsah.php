    <h2>Sport Španělska</h2>
    <h3>Fotbal</h3>
    <div class="obsah">
        <div class="foto"><img src="img/fotbal.jpg" alt="Fotka fotbalu"></div>
        <p>Fotbal je ve Španělsku národním fenoménem a neodmyslitelnou součástí kultury. Kluby jako Real Madrid a FC Barcelona patří k nejslavnějším na světě a španělská liga La Liga je známá svou vysokou kvalitou i technickým stylem hry. Velké úspěchy slaví také španělská reprezentace, která se zapsala do historie vítězstvími na mistrovství Evropy i světa.</p>
    </div>
    <h3>Tenis</h3>
    <div class="obsah">
        <p>Španělsko patří mezi tenisové velmoci, především díky dlouhé tradici a úspěšným hráčům. Největší ikonou je Rafael Nadal, jeden z nejlepších tenistů všech dob, zejména na antukových kurtech. Tenis je ve Španělsku velmi populární a hraje se na všech úrovních, od amatérských turnajů až po prestižní mezinárodní soutěže.</p>
