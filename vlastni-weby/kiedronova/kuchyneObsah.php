    <h2>Španělská kuchyně </h2>
    <h3>Tapas</h3>
    <div class="obsah">
        <div class="foto"><img src="img/tapas.jpg" alt="Fotka filmu"></div>
        
        <p>Tapas představují pestrý svět malých španělských jídel, která se tradičně podávají jako doplněk k vínu či pivu. Mohou mít podobu jednoduchých oliv, sýrů a sušené šunky, ale i propracovanějších teplých pokrmů z masa, ryb nebo zeleniny. Tapas nejsou jen o jídle, ale hlavně o sdílení, setkávání a pohodové atmosféře.</p>
    </div>
    <h3 style="text-align:end;">Paella</h3>
    <div class="obsah">
        <p>Paella je ikonický pokrm španělské kuchyně, jehož původ sahá do oblasti Valencie. Základem je rýže vařená se šafránem, který jí dodává typickou barvu a vůni. Paella se připravuje v mnoha variantách – s mořskými plody, kuřecím či králičím masem nebo zeleninou. Tradičně se podává přímo z pánve a je symbolem společného stolování.</p>
        <div class="foto"><img src="img/paella.jpg" alt="Fotka filmu"></div>    
    </div>
    <h3>Gazpacho</h3>
    <div class="obsah">
        <div class="foto"><img src="img/Gazpacho.jpg" alt="Fotka filmu"></div>
        
        <p>Gazpacho je osvěžující studená polévka pocházející z Andalusie. Připravuje se z čerstvých rajčat, paprik, okurek, česneku, olivového oleje a octa. Díky své lehkosti a chladivé chuti je ideální zejména v horkých letních dnech. Gazpacho je nejen chutné, ale také zdravé a plné vitamínů.</p>
