    <h2>Historie Srbska</h2>

    <p>Historie Srbska patří mezi nejbohatší a nejkomplexnější v jihovýchodní Evropě.
        Země byla po staletí křižovatkou kultur, náboženství i politických vlivů,
        což výrazně ovlivnilo její vývoj i identitu.</p>

    <h3>Středověké Srbské státy</h3>
    <p>První srbské státní útvary vznikaly již v raném středověku.
        Největšího rozkvětu dosáhlo Srbsko ve 14. století za vlády
        dynastie Nemanjićů. V této době vzniklo mnoho klášterů,
        hradů a kulturních památek, které jsou dodnes symbolem
        srbské historie.</p>

    <h3>Osmanská nadvláda</h3>
    <p>Po porážce v bitvě na Kosově poli roku 1389 se Srbsko postupně
        dostalo pod nadvládu Osmanské říše. Toto období trvalo několik
        staletí a výrazně ovlivnilo náboženství, architekturu i každodenní život.
        Přesto si Srbové dokázali uchovat svou kulturu a pravoslavnou víru.</p>

    <h3>Obnova státu v 19. století</h3>
    <p>V 19. století došlo k národním povstáním, která vedla
        k postupnému osvobození od Osmanské říše. Srbsko se stalo
        samostatným královstvím a začalo se modernizovat podle
        evropských vzorů.</p>

    <h3>20. století a Jugoslávie</h3>
    <p>Po první světové válce se Srbsko stalo součástí Království Jugoslávie,
        později Socialistické federativní republiky Jugoslávie.
        Toto období bylo plné politických změn, ale i hospodářského
        a kulturního rozvoje.</p>

    <h3>Moderní Srbsko</h3>
    <p>Po rozpadu Jugoslávie v 90. letech 20. století prošlo Srbsko
        složitým obdobím transformace. Dnes je Srbsko samostatným státem,
        který se snaží spojit svou bohatou historii s moderním vývojem.</p>
