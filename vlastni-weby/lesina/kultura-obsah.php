    <h2>Kultura Srbska</h2>

    <p>Srbská kultura je výsledkem dlouhého historického vývoje
        a prolínání východních i západních vlivů. Velký důraz je kladen
        na tradice, rodinu, náboženství a pohostinnost.</p>

    <h3>Tradice a zvyky</h3>
    <p>Jedním z nejvýznamnějších srbských zvyků je <strong>Slava</strong>,
        rodinný svátek zasvěcený patronovi rodiny. Tento zvyk je
        unikátní pro Srbsko a je zapsán na seznamu UNESCO.</p>

    <h3>Náboženství</h3>
    <p>Většina obyvatel Srbska se hlásí k pravoslavnému křesťanství.
        Srbská pravoslavná církev hraje významnou roli v kulturním
        i historickém životě země. Kláštery jako Studenica nebo Žiča
        patří mezi nejcennější památky.</p>

    <h3>Hudba a tanec</h3>
    <p>Tradiční hudba je založena na lidových nástrojích, jako je
        gusle nebo harmonika. Typické jsou také lidové tance
        zvané <em>kolo</em>, které se tančí v kruhu.</p>

    <h3>Kuchyně</h3>
    <p>Srbská kuchyně je vydatná a chutná. Mezi nejznámější pokrmy
        patří ćevapčići, pljeskavica, sarma nebo ajvar.
        Jídlo má v srbské kultuře důležité místo a často je
        spojeno s rodinnými setkáními.</p>

    <h3>Současná kultura</h3>
    <p>Moderní Srbsko má živou kulturní scénu – film, hudbu,
        sport i výtvarné umění. Bělehrad je považován za jedno
        z kulturních center Balkánu.</p>
